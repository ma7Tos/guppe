print('Esse programa irá ler os 3 valores digitados, e lhe apresentar a soma dos quadrados dos mesmos.')

print('Por favor, digite o primeiro numero: ')
n1 = int(input())
print('Por favor, digite o segundo numero: ')
n2 = int(input())
print('Por favor, digite o terceiro numero: ')
n3 = int(input())

soma_quadrados = (n1**2 + n2**2 + n3**2)

print(f'A soma dos quadrados de {n1}, {n2} e {n3} é: ', soma_quadrados)

